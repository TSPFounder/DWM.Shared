-- Separate from WorldPackageExporter.cs's schema (WorldInfo/Blocks/Parameters/SimSamples),
-- which is the engineering-mechanism package format, not the economy ledger.

PRAGMA foreign_keys = ON;

-- ------------------------------------------------------------------
-- Communities
-- ------------------------------------------------------------------
CREATE TABLE Communities (
    CommunityId  TEXT PRIMARY KEY NOT NULL,   -- stable slug, e.g. 'mountain'
    Name         TEXT NOT NULL,               -- display name, e.g. 'Mountain'
    BiomeType    TEXT NOT NULL,               -- 'Mountain' | 'Hillside' | 'Valley' | 'Suburb' | 'City'
    Description  TEXT
);

-- ------------------------------------------------------------------
-- Resources
-- ------------------------------------------------------------------
CREATE TABLE Resources (
    ResourceId  TEXT PRIMARY KEY NOT NULL,    -- stable slug, e.g. 'grain'
    Name        TEXT NOT NULL,                -- display name, e.g. 'Grain'
    Unit        TEXT NOT NULL,                -- e.g. 'unit', 'kWh', 'hour'
    Category    TEXT                          -- e.g. 'raw', 'manufactured', 'labor', 'power'
);

-- ------------------------------------------------------------------
-- CommunityResources — what each community produces or needs
-- ------------------------------------------------------------------
CREATE TABLE CommunityResources (
    CommunityId  TEXT NOT NULL REFERENCES Communities(CommunityId),
    ResourceId   TEXT NOT NULL REFERENCES Resources(ResourceId),
    Role         TEXT NOT NULL CHECK (Role IN ('Produces', 'Needs')),
    Quantity     REAL NOT NULL DEFAULT 0,     -- starting stock (Produces) or target/threshold (Needs)
    PRIMARY KEY (CommunityId, ResourceId, Role)
);

-- ------------------------------------------------------------------
-- StoneLedger — mutual-credit transaction log. APPEND-ONLY.
-- Single-row transfer model: every row moves Amount from FromCommunityId to
-- ToCommunityId. Network sum = 0 by construction. Never UPDATE/DELETE a row;
-- corrections are new offsetting rows.
-- ------------------------------------------------------------------
CREATE TABLE StoneLedger (
    TransactionId    TEXT PRIMARY KEY NOT NULL,   -- GUID or ULID, generated by the writer
    Timestamp        TEXT NOT NULL,               -- ISO-8601 UTC
    FromCommunityId  TEXT NOT NULL REFERENCES Communities(CommunityId),
    ToCommunityId    TEXT NOT NULL REFERENCES Communities(CommunityId),
    Amount           REAL NOT NULL CHECK (Amount > 0),  -- Stones transferred; always positive, direction is From->To
    ResourceId       TEXT REFERENCES Resources(ResourceId),  -- what was traded, if this row is trade-settlement (nullable)
    Quantity         REAL,                        -- quantity of ResourceId exchanged (nullable)
    Memo             TEXT,
    CHECK (FromCommunityId <> ToCommunityId)       -- a community cannot pay itself
);
CREATE INDEX idx_stoneledger_from ON StoneLedger(FromCommunityId);
CREATE INDEX idx_stoneledger_to   ON StoneLedger(ToCommunityId);

-- ------------------------------------------------------------------
-- Dollar Vault — finite outside-resource currency, modeled as a ledger
-- (append-only) so the running balance is always derivable and auditable.
-- ------------------------------------------------------------------
CREATE TABLE DollarVaultLedger (
    EntryId    TEXT PRIMARY KEY NOT NULL,
    Timestamp  TEXT NOT NULL,
    DeltaAmount REAL NOT NULL,      -- positive = inflow, negative = outflow
    Reason     TEXT
);

CREATE TABLE DollarVaultConfig (
    ConfigId                    INTEGER PRIMARY KEY CHECK (ConfigId = 1),  -- singleton row
    CascadingFailureThreshold   REAL NOT NULL
);

